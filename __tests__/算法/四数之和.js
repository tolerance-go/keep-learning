const fourSum = require("../../src/算法/双指针/四数之和");

test("fourSum([-3,-2,-1,0,0,1,2,3], 0)", () => {
  expect(fourSum([-3, -2, -1, 0, 0, 1, 2, 3], 0)).toStrictEqual([
    [-3, -2, 2, 3],
    [-3, -1, 1, 3],
    [-3, 0, 0, 3],
    [-3, 0, 1, 2],
    [-2, -1, 0, 3],
    [-2, -1, 1, 2],
    [-2, 0, 0, 2],
    [-1, 0, 0, 1],
  ]);
});
